    public void createTableMateriales(String sql)
    {
        modeloTablaM.setRowCount(0);
        try
        {
            int i = 0;
            ResultSet rs = RDB.query(Gestor.connect(1),sql);
            while(rs.next())
            {
                modeloTablaM.setRowCount(modeloTablaM.getRowCount() + 1);
                modeloTablaM.setValueAt(rs.getInt("codMaterial"),i,0);
                modeloTablaM.setValueAt(rs.getString("tipo"),i,1);
                modeloTablaM.setValueAt(rs.getFloat("precio"),i,2);
                i++;
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }
    public void createTableSilla(String sql)
    {
        modeloTablaS.setRowCount(0);
        try
        {
            int i = 0;
            ResultSet rs = RDB.query(Gestor.connect(1),sql);
            while(rs.next())
            {
                Material m = Gestor.getMaterial(rs.getInt("codMaterial"));
                
                modeloTablaS.setRowCount(modeloTablaS.getRowCount() + 1);
                modeloTablaS.setValueAt(m.getCod(),i,0);
                modeloTablaS.setValueAt(rs.getInt("id"),i,1);
                modeloTablaS.setValueAt(rs.getString("estilo"),i,2);
                if(rs.getInt("giratoria")==1){
                  modeloTablaS.setValueAt("Si",i,3);  
                }else{
                     modeloTablaS.setValueAt("No",i,3);  
                }
                if(rs.getInt("reclinable")==1){
                   modeloTablaS.setValueAt("SI",i,4);  
                }else{
                     modeloTablaS.setValueAt("NO",i,4);
                }
               
                modeloTablaS.setValueAt(m.getPrecio(),i,5);
                i++;
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }
